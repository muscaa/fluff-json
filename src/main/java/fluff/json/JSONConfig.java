package fluff.json;

/**
 * A configuration class for setting various parameters for JSON serialization.
 */
public class JSONConfig {
    
    /**
     * The tab character(s) used for pretty-printing JSON.
     */
    public static String PRETTY_TAB = "    ";
    
    /**
     * The new line character(s) used for pretty-printing JSON.
     */
    public static String PRETTY_NEW_LINE = "\n";
    
    /**
     * The colon character(s) used for pretty-printing JSON.
     */
    public static String PRETTY_COLON = ": ";
    
    /**
     * The comma character(s) used for pretty-printing JSON.
     */
    public static String PRETTY_COMMA = ",";
}
